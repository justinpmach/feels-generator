import { Box, Slide, Button } from "@chakra-ui/react";
import { type NextPage } from "next";
import Head from "next/head";
import Image from "next/image";
import { useState } from "react";
// import { Button } from "~/component/Button";
// import Carousel from "~/component/Carousel";
import { FormGroup } from "~/component/FormGroup";
import { Input } from "~/component/Input";
import { api } from "~/utils/api";

const colors = [
  "blue",
  "red",
  "pink",
  "green",
  "orange",
  "yellow",
  "white",
  "black",
];

const shapes = ["square", "circle", "triangle", "diamond"];

const styles = [
  "claymorphic",
  "3d rendered",
  "pixelated",
  "illustrated color pencil",
];

const questions: Array<string> = [
  "Describe the image you want to output",
  "Pick Image Color",
  "Pick Image Shape",
  "Pick Image Style",
  "Pick Image Quantity",
];

const GeneratePage: NextPage = () => {
  const [currentIndex, setCurrentIndex] = useState(0);
  // const prevSlide = () => {
  //   const isFirstSlide = currentIndex === 0;
  //   const newIndex = isFirstSlide ? 1 : currentIndex - 1;
  //   setCurrentIndex(newIndex);
  // };

  // const nextSlide = () => {
  //   const isFirstSlide = currentIndex === 0;
  //   const newIndex = isFirstSlide ? 1 : currentIndex - 1;
  //   setCurrentIndex(newIndex);
  // };
  const [form, setForm] = useState({
    prompt: "",
    color: "",
    shape: "",
    style: "",
    quantity: "1",
  });
  const [error, setError] = useState("");
  const [showQuestion, setShowQuestion] = useState(0);
  const [imagesUrl, setImagesUrl] = useState<{ imageUrl: string }[]>([]);

  const generateImage = api.generate.generateImage.useMutation({
    onSuccess(data) {
      setImagesUrl(data);
    },
    onError(error) {
      setError(error.message);
    },
  });

  function handleFormSubmit(e: React.FormEvent) {
    e.preventDefault();
    setError("");
    // submit form data to backend
    generateImage.mutate({
      ...form,
      quantity: parseInt(form.quantity),
    });

    // setForm((prev) => ({ ...prev, prompt: "" }));
    //
  }

  // function handleNext(index: number) {
  //   if (index === 5) return;
  //   setShowQuestion(index++);
  // }
  // function handleBack(index: number) {
  //   if (index === 0) return;
  //   setShowQuestion(index--);
  // }

  function updateForm(key: string) {
    return function (e: React.ChangeEvent<HTMLInputElement>) {
      setForm((prev) => ({
        ...prev,
        [key]: e.target.value,
      }));
    };
  }

  const prevSlide = () => {
    setCurrentIndex((current) =>
      current === 0 ? questions.length - 1 : current - 1
    );
  };
  const nextSlide = () => {
    // onToggle();
    setCurrentIndex((current) =>
      current === questions.length - 1 ? 0 : current + 1
    );
  };

  function handleSlides(index: number) {
    return currentIndex === index;
  }
  return (
    <>
      <Head>
        <title>Generate Feels Image</title>
        <meta name="description" content="Generated by create-t3-app" />
      </Head>

      <main>
        <div className="container mt-12 flex min-h-screen flex-col gap-4 px-8">
          <h1 className="text-3xl">Generate Your Image</h1>

          <form className="flex w-full flex-col" onSubmit={handleFormSubmit}>
            {questions.map((q, index) => (
              <Slide
                key={index}
                direction="right"
                in={handleSlides(index)}
                style={{
                  zIndex: 10,
                }}
              >
                <Box
                  p="20px"
                  color="white"
                  mt="64"
                  bg="blue.400"
                  rounded="sm"
                  shadow="md"
                >
                  <FormGroup className="mb-4">
                    <label>
                      {currentIndex + 1}. {q}
                    </label>

                    {currentIndex === 0 && (
                      <Input
                        required
                        value={form.prompt}
                        onChange={updateForm("prompt")}
                      />
                    )}

                    {currentIndex === 1 && (
                      <>
                        {colors.map((color) => (
                          <label key={color} className="flex gap-2 text-2xl">
                            <input
                              required
                              type="radio"
                              name="color"
                              checked={color === form.color}
                              onChange={() =>
                                setForm((prev) => ({ ...prev, color }))
                              }
                            ></input>
                            {color}
                          </label>
                        ))}
                      </>
                    )}

                    {currentIndex === 2 &&
                      shapes.map((shape) => (
                        <label key={shape} className="flex gap-2 text-2xl">
                          <input
                            required
                            type="radio"
                            name="color"
                            checked={shape === form.shape}
                            onChange={() =>
                              setForm((prev) => ({ ...prev, shape }))
                            }
                          ></input>
                          {shape}
                        </label>
                      ))}

                    {currentIndex === 3 &&
                      styles.map((style) => (
                        <label key={style} className="flex gap-2 text-2xl">
                          <input
                            required
                            type="radio"
                            name="color"
                            checked={style === form.style}
                            onChange={() =>
                              setForm((prev) => ({ ...prev, style }))
                            }
                          ></input>
                          {style}
                        </label>
                      ))}

                    {currentIndex === 4 && (
                      <Input
                        required
                        inputMode="numeric"
                        pattern="[1-9]|10"
                        value={form.quantity}
                        onChange={updateForm("quantity")}
                      ></Input>
                    )}
                  </FormGroup>
                </Box>
              </Slide>
            ))}
            <div className="mt-30 flex-col-2 mx-auto flex flex-wrap justify-center gap-4">
              <Button
                className="z-20"
                disabled={currentIndex === 0}
                onClick={prevSlide}
              >
                Back
              </Button>
              {currentIndex === 4 ? (
                <Button
                  className="z-20"
                  disabled={generateImage.isLoading}
                  isLoading={generateImage.isLoading}
                >
                  Submit
                </Button>
              ) : (
                <Button className="z-20" onClick={nextSlide}>
                  Next
                </Button>
              )}
            </div>

            {error && (
              <div className="rounded bg-red-500 px-8 py-4 text-xl text-white">
                {error}
              </div>
            )}
          </form>

          {imagesUrl.length > 0 && (
            <>
              <h2 className="text-xl">Your Images</h2>
              <section className="mb-12 grid grid-cols-4 gap-4">
                {imagesUrl.map(({ imageUrl }) => (
                  <Image
                    key={imageUrl}
                    src={imageUrl}
                    alt="an image of generated prompt"
                    width="512"
                    height="512"
                    className="w-full"
                  />
                ))}
              </section>
            </>
          )}
        </div>
      </main>
    </>
  );
};

export default GeneratePage;
